  def distribution_cards
    cards = @deck[@position..@position+1]
    @position += 2
    cards
  end

  def more_card
    card = @deck[@position]
    @position += 1
    card
  end

  def shuffle_deck
    @deck = @deck.shuffle
    @position = 0
  end
end
